int		ft_iterative_factorial(int nb)
{
	int i;

	i = 1;
	if (nb >= 0 && nb <= 12)
	{
		while (nb > 1)
		{
			i = i * (nb);
			nb--;
		}
		return (i);
	}
	else
		return (0);
}
